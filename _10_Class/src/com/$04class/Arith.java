package com.$04class;

public class Arith {
    public int add(int num0, int num1) {
        return num0 + num1;
    }

    public int sub(int num0, int num1) {
        return num0 - num1;
    }

    public int mul(int num0, int num1) {
        return num0 * num1;
    }

    public int div(int num0, int num1) {
        return num0 / num1;
    }
}
